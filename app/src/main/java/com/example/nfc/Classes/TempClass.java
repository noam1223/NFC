package com.example.nfc.Classes;

public class TempClass {

    private String temp, time;


    public TempClass() {

    }

    public TempClass(String temp, String time) {
        this.temp = temp;
        this.time = time;
    }

    public String getTemp() {
        return temp;
    }

    public void setTemp(String temp) {
        this.temp = temp;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

}
